  end subroutine openclose_dc_vdc_files

  ! This subroutine write all V_dc into files
  ! (already opened with openclose_dc_vdc_files(1))
  ! Some information may also be written on the screen
  subroutine write_dc_vdc()
    use mod_f90_kind
    use mod_parameters, only: Npl,dc_fields,hw_count,mmlayermag
    use mod_magnet, only: mvec_spherical
    implicit none
    integer  :: i,iw,sigma

    do sigma=1,3 ; do i=1,Npl
      iw = 100000+(sigma-1)*Npl+i

      write(unit=iw,fmt="(a,2x,3(es16.9,2x))") trim(dc_fields(hw_count)) , vdc(sigma,i) , mvec_spherical(i,2) , mvec_spherical(i,3)
    end do ; end do

    do sigma=1,3
      iw = 110000+sigma

      write(unit=iw,fmt="(a,2x,3(es16.9,2x))") trim(dc_fields(hw_count)) , total_vdc(sigma) , mvec_spherical(mmlayermag(1)-1,2) , mvec_spherical(mmlayermag(1)-1,3)
    end do

    return
  end subroutine write_dc_vdc

  ! This subroutine sorts V_dc files
  subroutine sort_vdc()
    use mod_f90_kind
    use mod_parameters, only: Npl,itype
    use mod_tools, only: sort_file
    implicit none
    integer :: i,sigma,iw,idc=1

    ! Opening V_dc files
    if(itype.eq.9) then
      idc=10
      call openclose_dc_vdc_files(1)
    else
      call openclose_vdc_files(1)
    end if

    do sigma=1,3 ; do i=1,Npl
      iw = 10000*idc+(sigma-1)*Npl+i
      ! Sorting V_dc files
      call sort_file(iw,.true.)
    end do ; end do
    do sigma=1,3
      iw = 11000*idc+sigma
      ! Sorting total V_dc files
      call sort_file(iw,.true.)
    end do

    ! Closing V_dc files
    if(itype.eq.9) then
      call openclose_dc_vdc_files(2)
    else
      call openclose_vdc_files(2)
    end if

    return
  end subroutine sort_vdc

end module mod_vdc